export function BottomNav() {
  const pathname = usePathname();
  const { data: notifData } = useNotifications({ unread: true, limit: 1 });
  const hasUnread = (notifData?.items?.length ?? 0) > 0;

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-40 border-t border-gray-200 bg-white"
      style={{ paddingBottom: "env(safe-area-inset-bottom)" }}
    >
      <ul className="mx-auto flex max-w-lg items-center justify-around px-2 py-2">
        {NAV_ITEMS.map((item) => {
          const active =
            pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <li key={item.href} className="flex-1">
              <Link
                href={item.href}
                className={`relative flex flex-col items-center gap-0.5 py-1 text-xs font-medium transition-colors ${
                  active ? "text-forest-700" : "text-gray-400"
                }`}
                aria-current={active ? "page" : undefined}
              >
                {item.icon(active)}
                {item.href === "/notifications" && hasUnread && (
                  <span className="absolute right-4 top-0 h-2 w-2 rounded-full bg-forest-700" />
                )}
                {item.label}
              </Link>
            </li>
          );
        })}
      </ul>
    </nav>
  );
}
